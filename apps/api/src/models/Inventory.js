/*
|--------------------------------------------------------------------------
| Imports
|--------------------------------------------------------------------------
*/

import mongoose from "mongoose";

const {

    Schema,

    model

} = mongoose;

/*
|--------------------------------------------------------------------------
| Inventory Schema
|--------------------------------------------------------------------------
*/

const InventorySchema = new Schema(

    {

        /*
        |--------------------------------------------------------------------------
        | Shop
        |--------------------------------------------------------------------------
        */

        shop: {

            type: Schema.Types.ObjectId,

            ref: "Shop",

            required: true,

            index: true

        },

        /*
        |--------------------------------------------------------------------------
        | Product
        |--------------------------------------------------------------------------
        */

        product: {

            type: Schema.Types.ObjectId,

            ref: "Product",

            required: true,

            index: true

        },

        /*
        |--------------------------------------------------------------------------
        | Shopify Inventory Item
        |--------------------------------------------------------------------------
        */

        shopifyInventoryItemId: {

            type: String,

            required: true,

            index: true

        },

        /*
        |--------------------------------------------------------------------------
        | Shopify Location
        |--------------------------------------------------------------------------
        */

        shopifyLocationId: {

            type: String,

            required: true,

            index: true

        },

        /*
        |--------------------------------------------------------------------------
        | SKU
        |--------------------------------------------------------------------------
        */

        sku: {

            type: String,

            default: "",

            trim: true,

            index: true

        },

        /*
        |--------------------------------------------------------------------------
        | Barcode
        |--------------------------------------------------------------------------
        */

        barcode: {

            type: String,

            default: "",

            trim: true

        },

        /*
        |--------------------------------------------------------------------------
        | Quantity
        |--------------------------------------------------------------------------
        */

        quantity: {

            type: Number,

            default: 0,

            min: 0

        },

        /*
        |--------------------------------------------------------------------------
        | Reserved Quantity
        |--------------------------------------------------------------------------
        */

        reservedQuantity: {

            type: Number,

            default: 0,

            min: 0

        }

    },

    {

        timestamps: true,

        versionKey: false

    }

);
